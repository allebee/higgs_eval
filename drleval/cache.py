"""Content-addressed response cache for Anthropic `messages.create`.

Monkey-patches the Anthropic client (not the agent itself — we promised not
to modify the agent). Keys are a sha256 of (model, system, messages, tools,
max_tokens, temperature). When `DRLEVAL_CACHE_DIR` is set, the first call
with a given key writes to disk; subsequent calls with the same key read from
disk and never hit the API.

Enables deterministic replay: a trace can be regenerated byte-for-byte from a
cache directory with no API credits required. Turn it off by unsetting the
env var.
"""
from __future__ import annotations

import hashlib
import json
import os
import pickle
from pathlib import Path
from typing import Any


_INSTALLED = False
_HITS = 0
_MISSES = 0


def _key(payload: dict[str, Any]) -> str:
    # Canonicalize: sort keys, drop non-deterministic fields.
    canon: dict[str, Any] = {
        "model": payload.get("model"),
        "system": payload.get("system"),
        "messages": payload.get("messages"),
        "tools": payload.get("tools"),
        "tool_choice": payload.get("tool_choice"),
        "max_tokens": payload.get("max_tokens"),
        "temperature": payload.get("temperature"),
    }
    blob = json.dumps(canon, sort_keys=True, default=str).encode()
    return hashlib.sha256(blob).hexdigest()


def install(cache_dir: Path | None = None) -> None:
    """Monkey-patch `anthropic.Anthropic.messages.create` with a cache wrapper.

    Idempotent. Call once at process start (e.g. from CLI entrypoint).
    """
    global _INSTALLED
    if _INSTALLED:
        return
    cache_dir = cache_dir or Path(os.getenv("DRLEVAL_CACHE_DIR", ""))
    if not cache_dir or not str(cache_dir):
        return
    cache_dir = Path(cache_dir)
    cache_dir.mkdir(parents=True, exist_ok=True)

    try:
        from anthropic.resources.messages import Messages
    except Exception:
        return

    orig_create = Messages.create

    def cached_create(self: Any, **kwargs: Any) -> Any:
        global _HITS, _MISSES
        key = _key(kwargs)
        path = cache_dir / f"{key}.pkl"
        if path.exists():
            with path.open("rb") as f:
                _HITS += 1
                return pickle.load(f)
        resp = orig_create(self, **kwargs)
        # Best-effort cache write. Anthropic response objects are pydantic-like
        # and picklable in practice; if not, silently skip caching.
        try:
            with path.open("wb") as f:
                pickle.dump(resp, f)
        except Exception:
            try:
                path.unlink()
            except Exception:
                pass
        _MISSES += 1
        return resp

    Messages.create = cached_create  # type: ignore[assignment]
    _INSTALLED = True


def stats() -> dict[str, int]:
    return {"hits": _HITS, "misses": _MISSES}
